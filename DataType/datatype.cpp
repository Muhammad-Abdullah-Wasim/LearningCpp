#include <iostream>
using namespace std;
int main ()
{
    int a;
    char b;
    float c;
    double d;
    long e;
    cout << "Size of int is " << sizeof(a) << " bytes" << endl;
    cout << "Size of char is " << sizeof(b) << " bytes" << endl;
    cout << "Size of float is " << sizeof(c) << " bytes" << endl;
    cout << "Size of double is " << sizeof(d) << " bytes" << endl;
    cout << "Size of long is " << sizeof(e) << " bytes" << endl;
    return 0;
}